#include <bits/stdc++.h>

using namespace std;

int main() {
	ios::sync_with_stdio(false);
	cin.tie(0);
	int tt;
	cin >> tt;
	while (tt--) {
		int n;
		cin >> n;
		// There are 'n' places you can put a vertical diamond, 
		// so the answer is 'n' for each test case.
		cout << n << '\n';
	}
	return 0;
}
